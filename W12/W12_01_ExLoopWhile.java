package W12;

/**
 * W12_01_WhileLoop
 */
public class W12_01_ExLoopWhile {

    public static void main(String[] args) {

        // TODO 1
        // --------------------------------------------------------------------------------------------------------
        int result = 0;
        int time = 1;

        System.out.println("\n-----------------------------------------------------------------\n");

        while (time < 5) {
            System.out.printf("Time: %d Hello World %n", time);
            time++;
        }

        // TODO 2
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        time = 1;
        while(time <= 10){
            System.out.printf("%d", time);
            time++;
        }

        // TODO 3
        // --------------------------------------------------------------------------------------------------------
        System.out.println("\n-----------------------------------------------------------------\n");

        time = 1;
        while(time <= 10)
        {
            result = (result + time);
            if (time < 10) {
                System.out.printf("%d ", time);
            } else {
                System.out.printf("%d = %d", time, result);
            }
            time++;
        }

        // TODO 4
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");

        result = 0;
        time = 1;
        while(time <= 10) {
            result = (result + time);
            if (time < 10) {
                System.out.printf("%d+", time);
            } else {
                System.out.printf("%d = %d", time, result);
            }
            time++;
        }

        // TODO 5
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        result = 10;
        time = 1;
        while(time <= 10) {
            System.out.printf("%d ", result);
            result--;
            time++;
        }

        // TODO 6
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        result = 1;
        time = 1;
        while(time <= 10) {
            System.out.printf("%d ", result);
            result += 2;
            time++;
        }

        // TODO 7
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        result = 0;
        time = 1;
        while(time <= 10) {
            result += 2;
            System.out.printf("%d ", result);
            time++;
        }

        // TODO 8
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n----------------------------------------------------------------\n");
        result = 0;
        time = 1;
        while (time <= 10) {
            result = time * time;
            System.out.printf("%d ", result);
            time++;
        }

        // TODO 9
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        result = 1;
        time = 1;

        while(time <= 10) {
            result = time * time;
            System.out.printf("%d ", result);
        }

        // TODO 10
        // --------------------------------------------------------------------------------------------------------

        System.out.println("\n-----------------------------------------------------------------\n");
        result = 1;
        time = 1;
        while (time < 5) {
            time++;
            if (time % 2 == 0) {
                System.out.printf("%d Even %n", time);
            } else {
                System.out.printf("%d Odd %n", time);
            }
        }

        // --------------------------------------------------------------------------------------------------------
        System.out.println("\n-----------------------------------------------------------------\n");

        time = 1;
        while (time <= 5) {
            System.out.println("Hello While: " + time);
            time++;
        }

        for (int i = 1; i <= 5; i++) {
            System.out.println("Hell Int: " + i);
        }
    }
}